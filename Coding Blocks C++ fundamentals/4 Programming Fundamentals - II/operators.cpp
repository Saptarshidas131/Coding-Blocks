#include<iostream>
using namespace std;

int main()
{
    int a=5,b=6;

    // bitwise or
  cout<<(a|b)<<endl;
  // bitwise and
  cout<<(a&b)<<endl;
  // bitwise xor
  cout<<(a^b)<<endl;
  // bitwise not
  cout<<(~0)<<" "<<(~1)<<endl;
  // left shift
  cout<<(a<<1)<<endl;
  // right shift
  cout<<(b>>1)<<endl;

  return 0;
}
